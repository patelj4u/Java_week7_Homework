
//Declare multidimensional array and store 5 countries and their capital and print them in console.

public class Pro3_Multi_Array {
    public static void main(String[] args) {

        String[][] n = {{"India", "Delhi"},
                        {"UK", "london"},
                        {"USA", "Washington D.C"},
                        {"France", "Paris"},
                        {"Poland", "Warsaw"}};

        System.out.println("Country is : " + n[0][0] + " Capital is :" +n[0][1]);
        System.out.println("Country is : " + n[1][0] + " Capital is :" +n[1][1]);
        System.out.println("Country is : " + n[2][0] + " Capital is :" +n[2][1]);
        System.out.println("Country is : " + n[3][0] + " Capital is :" +n[3][1]);
        System.out.println("Country is : " + n[4][0] + " Capital is :" +n[4][1]);



    }
}
